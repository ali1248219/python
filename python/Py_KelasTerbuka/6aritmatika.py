# operasi aritmatika

'''
    urutannya :
    1. ()
    2. eksponen **
    3. perkalian dan teman2 * / % //
    4. tambah kurang + -
'''

a = 10
b = 6

# operasi tambah 

hasil = a + b
print (f"{a} + {b} = {hasil}")

# operasi kurang
hasil = a - b
print (f"{a} - {b} = {hasil}")

# operasi perkalian
hasil = a * b
print (f"{a} * {b} = {hasil}")

# operasi pembagian
hasil = a / b
print (f"{a} / {b} = {hasil}") 
#  hasilnya berupa float

# operasi eksponen (pangkat)
hasil = a ** b
print (f"{a} ** {b} = {hasil}") 

# operasi modulus / sisa pembagian
hasil = a % b
print (f"{a} % {b} = {hasil}") 

# operasi floor division / pembagian pembulatan ke bawah
hasil = a // b
print (f"{a} // {b} = {hasil}") 