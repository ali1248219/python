# temperatur

# celcius ke satuan lain

print ("\n PROGRAM KONVERSI TEMPERATUR\n")

celcius = float(input('masukan suhu dalam celcius : '))
print (f"suhu adalah : {celcius} celcius")

# reamur, 4/5c

reamur = (4/5) * celcius
print (f"suhu dalam reamur {reamur} reamur")

# fahrenheit

fahrenheit = ((9/5) * celcius)+32
print (f"suhu dalam fahrenheit {fahrenheit} fahrenheit")

# kelvin

kelvin = celcius + 273
print (f"suhu dalam kelvin {kelvin} kelvin")