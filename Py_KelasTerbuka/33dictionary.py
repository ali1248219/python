# list adalah contoh array, kita akses data dengan index

data_list = ["otong","dudung","otot"]
print(data_list[0])

# dictionary (dict) -> associative array, kita mengakses menggunakan identifier (key)

data_dict = {
    'key' : 'value',
    'nama' : 'fj',
    'umur' : 24
}
print(data_dict , "\n")
print(data_dict['key'])

# ini akan berguna ketika kita punya struktur data